public class Calculadora {

	/**
	 * 
	 */
	public Calculadora() {

	}

	public double suma(double num1, double num2) {

		return num1 + num2;
	}

	public double resta(double num1, double num2) {

		return num2 - num1;
	}

	public double multi(double num1, double num2) {

		return num1 * num2;
	}
}
